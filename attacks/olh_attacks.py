"""
Poisoning Attack Strategies for OLH
=====================================
Three attack strategies targeting the OLH (Optimized Local Hashing) protocol.

In OLH, each report is a tuple (sanitized_value, rnd_seed) where:
  - rnd_seed identifies a hash function h: [d] → [g].
  - sanitized_value ∈ [0, g-1] is the GRR-perturbed hash of the true value.

An item v is "supported" by report (y, seed) if h_seed(v) == y. The adversary
crafts (y, seed) pairs to strategically support effective attack items without
supporting any target items (to avoid directly "helping" items in a detectable
way while still manipulating ranking).

  1. Random Attack  — Fake users report a target item via LH_Client.
  2. ROA            — Random Output Attack: uses random (y, seed) pairs
                      that support only non-target items.
  3. Greedy Attack  — Selects (seed, y) combinations minimizing cost, where
                      cost is inversely proportional to how many effective
                      attack items the combination supports.
"""

import numpy as np
import random
from sys import maxsize
from typing import Dict, List, Optional, Tuple
import xxhash

from ..core.olh import LH_Client


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------

def _g(epsilon: float) -> int:
    return int(round(np.exp(epsilon))) + 1


def _hash(value: int, seed: int, g: int) -> int:
    return xxhash.xxh32(str(value), seed=seed).intdigest() % g


def _effective_items(A, T, freq):
    return [a for a in A if any(freq[t] > freq[a] for t in T)]


def _calculate_costs(
    target_items: List[int],
    eff_items: List[int],
    freq: Dict[int, int],
    hash_funcs: List[int],
    g: int,
) -> Tuple[Optional[int], Optional[int]]:
    """
    Find the (seed, hashed_value) pair that minimizes attack cost.

    Cost is defined as 1 / sum(1 / dist(a, nearest_target)) over all effective
    attack items supported by (seed, hashed_value). Lower cost = higher impact.

    Only (seed, y) pairs that do NOT support any target item are considered,
    ensuring the fake reports do not inadvertently inflate target items.

    Args:
        target_items: Target item IDs.
        eff_items: Effective attack item IDs.
        freq: Current frequency estimates.
        hash_funcs: List of random seeds (hash function identifiers).
        g: Hash output domain size.

    Returns:
        Tuple of (best_seed, best_hashed_value) or (None, None) if no valid pair.
    """
    min_cost = float("inf")
    best_seed, best_y = None, None

    for seed in hash_funcs:
        for y in range(g):
            # Skip if this (seed, y) supports any target item
            if any(_hash(t, seed, g) == y for t in target_items):
                continue

            # Compute support score for effective attack items
            score = 0.0
            for a in eff_items:
                if _hash(a, seed, g) == y:
                    dists = [freq[t] - freq[a] for t in target_items if freq[t] > freq[a]]
                    if dists:
                        score += 1.0 / min(dists)

            if score > 0:
                cost = 1.0 / score
                if cost < min_cost:
                    min_cost = cost
                    best_seed = seed
                    best_y = y

    return best_seed, best_y


