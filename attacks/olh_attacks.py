def random_attack_olh(
    n2: int,
    target_items: List[int],
    d: int,
    epsilon: float,
) -> List[Tuple[int, int]]:
    """
    Random Attack for OLH.

    Each fake user selects a target item uniformly at random and submits a
    report generated by LH_Client as if they were an honest user with that
    value. This directly inflates target item support.

    Args:
        n2: Number of fake users.
        target_items: List of target item IDs.
        d: Domain size.
        epsilon: Privacy budget.

    Returns:
        List of n2 (sanitized_value, rnd_seed) tuples.

    Example:
        >>> fake = random_attack_olh(n2=500, target_items=[2,5], d=100, epsilon=2.0)
    """
    fake_data = []
    for _ in range(n2):
        chosen = int(np.random.choice(target_items))
        fake_data.append(LH_Client(chosen, d, epsilon))
    return fake_data


def roa_attack_olh(
    n2: int,
    non_target_items: List[int],
    d: int,
    epsilon: float,
) -> List[Tuple[int, int]]:
    """
    Random Output Attack (ROA) for OLH.

    Each fake user generates a random seed and reports the hash of a randomly
    selected non-target item under that seed. The resulting (y, seed) pair
    supports only non-target items, serving as a noise baseline.

    Args:
        n2: Number of fake users.
        non_target_items: List of non-target item IDs.
        d: Domain size.
        epsilon: Privacy budget.

    Returns:
        List of n2 (sanitized_value, rnd_seed) tuples.

    Example:
        >>> fake = roa_attack_olh(n2=500, non_target_items=[0,1,3,...],
        ...     d=100, epsilon=2.0)
    """
    g = _g(epsilon)
    fake_data = []
    for _ in range(n2):
        seed = np.random.randint(0, maxsize, dtype=np.int64)
        chosen = int(np.random.choice(non_target_items))
        y = _hash(chosen, seed, g)
        fake_data.append((y, seed))
    return fake_data


def greedy_attack_olh(
    n2: int,
    target_items: List[int],
    non_target_items: List[int],
    expected_perturbed_freq: Dict[int, int],
    d: int,
    epsilon: float,
    hash_funcs: Optional[List[int]] = None,
    num_hash_funcs: int = 100,
) -> List[Tuple[int, int]]:
    """
    Greedy Attack for OLH.

    At each step, finds the (seed, y) pair with the lowest cost (highest
    combined impact on effective attack items) and injects enough fake users
    to push the most cost-effective item above its nearest target.

    The trick: only pairs that do NOT hash any target item to y are considered.
    This means fake votes for non-targets don't accidentally boost targets, and
    the attack avoids a statistical signature that could be detected by the
    server comparing target item support patterns.

    Args:
        n2: Number of fake users.
        target_items: Target item IDs (T).
        non_target_items: Non-target item IDs (A).
        expected_perturbed_freq: Expected perturbed frequency estimates.
        d: Domain size.
        epsilon: Privacy budget.
        hash_funcs: Pre-generated random seeds to use as hash functions.
                    If None, generates `num_hash_funcs` random seeds.
        num_hash_funcs: Number of random hash functions to sample (default 100).

    Returns:
        List of (sanitized_value, rnd_seed) tuples.

    Example:
        >>> fake = greedy_attack_olh(n2=500, target_items=[2,5],
        ...     non_target_items=list(range(100)), expected_perturbed_freq=epf,
        ...     d=100, epsilon=2.0)
    """
    g = _g(epsilon)

    if hash_funcs is None:
        hash_funcs = [
            int(np.random.randint(0, maxsize, dtype=np.int64))
            for _ in range(num_hash_funcs)
        ]

    attacked_freq = expected_perturbed_freq.copy()
    fake_data = []
    optimal_combinations = []
    eff_items = _effective_items(non_target_items, target_items, attacked_freq)

    while n2 > 0 and eff_items:
        best_seed, best_y = _calculate_costs(
            target_items, eff_items, attacked_freq, hash_funcs, g
        )
        if best_seed is None:
            break

        optimal_combinations.append((best_y, best_seed))

        # Find the effective attack item with minimum distance supported by this hash
        min_diff = float("inf")
        min_diff_item = None
        for a in eff_items:
            if _hash(a, best_seed, g) == best_y:
                dists = [attacked_freq[t] - attacked_freq[a] for t in target_items if attacked_freq[t] > attacked_freq[a]]
                diff = min(dists, default=float("inf"))
                if diff < min_diff:
                    min_diff = diff
                    min_diff_item = a

        if min_diff_item is None or min_diff == float("inf"):
            break

        steps = min(int(min_diff), n2)
        fake_data.extend([(best_y, best_seed)] * steps)

        # Update frequencies for all supported items
        for a in eff_items:
            if _hash(a, best_seed, g) == best_y:
                attacked_freq[a] += steps
        for t in target_items:
            if _hash(t, best_seed, g) == best_y:
                attacked_freq[t] += steps

        n2 -= steps
        eff_items = _effective_items(non_target_items, target_items, attacked_freq)

    if n2 > 0 and optimal_combinations:
        extra = random.choices(optimal_combinations, k=n2)
        fake_data.extend(extra)
    elif n2 > 0:
        fake_data.extend(roa_attack_olh(n2, non_target_items, d, epsilon))

    return fake_data
